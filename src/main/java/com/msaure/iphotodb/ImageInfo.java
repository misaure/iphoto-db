package com.msaure.iphotodb;

public class ImageInfo implements java.io.Serializable {
    
    private static final long serialVersionUID = 3259248422550110602L;

    private int id;
    private String imagePath;
    private String thumbPath;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getThumbPath() {
        return thumbPath;
    }

    public void setThumbPath(String thumbPath) {
        this.thumbPath = thumbPath;
    }

    @Override
    public String toString() {
        return "ImageInfo{" + "imagePath=" + imagePath + ", thumbPath=" + thumbPath + '}';
    }
    
}
